#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned char	*a;
	unsigned char	*b;

	a = (unsigned char *)s1;
	b = (unsigned char *)s2;
	while (n--)
	{
		if (*a == '\0' && *b == '\0')
			return (0);
		if (*a != *b)
			return (*a - *b);
		a++;
		b++;
	}
	return (0);
}
